package com.yqbing.servicebing.webapp.response;

import java.io.Serializable;
import java.util.List;

public class HomeModel  implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 88807942260951493L;
	
	
	public String name;
	public Long pid;
	public Integer style;
	public Integer isMore;
	public Integer isMark;
	public List<HomeAppRes> apps;
	
	
	public Integer getIsMark() {
		return isMark;
	}
	public void setIsMark(Integer isMark) {
		this.isMark = isMark;
	}
	public Integer getIsMore() {
		return isMore;
	}
	public void setIsMore(Integer isMore) {
		this.isMore = isMore;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getPid() {
		return pid;
	}
	public void setPid(Long pid) {
		this.pid = pid;
	}
	public Integer getStyle() {
		return style;
	}
	public void setStyle(Integer style) {
		this.style = style;
	}
	public List<HomeAppRes> getApps() {
		return apps;
	}
	public void setApps(List<HomeAppRes> apps) {
		this.apps = apps;
	}

}
