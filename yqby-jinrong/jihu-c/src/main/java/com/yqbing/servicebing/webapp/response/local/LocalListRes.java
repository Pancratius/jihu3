package com.yqbing.servicebing.webapp.response.local;

import java.util.List;

import com.yqbing.servicebing.repository.database.bean.LocalLabel;

public class LocalListRes {
    
	private Long id;
	private String img;
	private String title;
	private String desc;
	private String foodType;
	private String address;
	private String name;//点名字
	private List<LocalLabel> label;
	private String moduleType;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getModuleType() {
		return moduleType;
	}
	public void setModuleType(String moduleType) {
		this.moduleType = moduleType;
	}
	public List<LocalLabel> getLabel() {
		return label;
	}
	public void setLabel(List<LocalLabel> label) {
		this.label = label;
	}
	public String getFoodType() {
		return foodType;
	}
	public void setFoodType(String foodType) {
		this.foodType = foodType;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
