package com.yqbing.servicebing.repository.database.bean;

import java.util.Date;

public class FreeScale {
    private Integer id;

    private Integer amount;

    private Integer storeScale;

    private Integer actScale;

    private Integer type;

    private Date createTime;

    private Date updateTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAmount() {
        return amount;
    }

    public void setAmount(Integer amount) {
        this.amount = amount;
    }

    public Integer getStoreScale() {
        return storeScale;
    }

    public void setStoreScale(Integer storeScale) {
        this.storeScale = storeScale;
    }

    public Integer getActScale() {
        return actScale;
    }

    public void setActScale(Integer actScale) {
        this.actScale = actScale;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}