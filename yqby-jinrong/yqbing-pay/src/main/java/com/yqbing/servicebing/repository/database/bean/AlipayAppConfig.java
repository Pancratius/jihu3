package com.yqbing.servicebing.repository.database.bean;

import java.io.Serializable;

/**
 * Title:AlipayAppConfig.java
 * Description:支付宝配置
 * @author alexaz
 * 2019年7月16日
 */
public class AlipayAppConfig implements Serializable{

	private static final long serialVersionUID = 1L;
	/**
	 * 支付宝支付请求url
	 */
	private String url;
	/**
	 * appid
	 */
	private String appId;
	/**
	 * 私钥
	 */
	private String appPrivateKey;
	/**
	 * 公钥
	 */
	private String alipayPublicKey;
	/**
	 * 签名类型
	 */
	private String signType;


	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAppId() {
		return appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}

	public String getAppPrivateKey() {
		return appPrivateKey;
	}

	public void setAppPrivateKey(String appPrivateKey) {
		this.appPrivateKey = appPrivateKey;
	}

	public String getAlipayPublicKey() {
		return alipayPublicKey;
	}

	public void setAlipayPublicKey(String alipayPublicKey) {
		this.alipayPublicKey = alipayPublicKey;
	}

	public String getSignType() {
		return signType;
	}

	public void setSignType(String signType) {
		this.signType = signType;
	}

	@Override
	public String toString() {
		return "AlipayChannelConfig [url=" + url + ", appId=" + appId + ", appPrivateKey=xxx, alipayPublicKey=xxx, signType=" + signType + "]";
	}

	
	
	
}
