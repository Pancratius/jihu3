package com.yqbing.servicebing.repository.database.bean;

import java.util.List;

import com.yqbing.servicebing.webapp.request.pay.po.PayChannel;
import com.yqbing.servicebing.webapp.request.pay.po.PayProvider;


public class PayProviderInfo extends PayProvider {

	private static final long serialVersionUID = 1L;

	/**
	 * 支付渠道列表
	 */
	protected List<PayChannel> payChannels;

	public List<PayChannel> getPayChannels() {
		return payChannels;
	}

	public void setPayChannels(List<PayChannel> payChannels) {
		this.payChannels = payChannels;
	}

	@Override
	public String toString() {
		return "PayProviderInfo [payChannels=" + payChannels + ", payProvider=" + payProvider + ", payProviderDesc=" + payProviderDesc + ", payProviderStatus="
				+ payProviderStatus + "]";
	}
	
	
}
