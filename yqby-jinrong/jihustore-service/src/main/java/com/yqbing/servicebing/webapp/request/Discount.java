package com.yqbing.servicebing.webapp.request;

public class Discount {

	public Long id;
	public int number;
	public int ral;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getRal() {
		return ral;
	}
	public void setRal(int ral) {
		this.ral = ral;
	}
	
	
	
}
